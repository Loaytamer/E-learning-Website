let course_index = localStorage.getItem("course_num");
// console.log(typeof course_content.links[0])
let course_name = document.getElementById("course_name");
course_name.textContent = course_content[course_index].name;
let video_title = document.getElementById("video_title");
let video = document.getElementById("vid");
let vid_index = 0;
let container = document.getElementById("content_list");
function create_content() {
  video.src = course_content[course_index].links[0];
  video_title.textContent = course_content[course_index].vid_names[0];
  for (
    let index = 0;
    index < course_content[course_index].links.length;
    index++
  ) {
    let element = document.createElement("li");
    let link = document.createElement("button");
    link.textContent = course_content[course_index].vid_names[index];
    link.style = `
        background-color:#fff;
        border:none;
        `;
    let icon_img = document.createElement("img");
    icon_img.src = "../images/check.png";
    icon_img.style = `
        width:40px;
        height:40px;
        margin-right:10px;
        `;
    element.style = `
        margin-bottom:15px;
        `;
    element.appendChild(icon_img);
    element.appendChild(link);
    container.appendChild(element);
  }
}
create_content();
let mark_btn = document.getElementById("mark");
mark_btn.addEventListener("click", function () {
  // let selected =document.getElementById('content_list');
  let numb = document.getElementById("content_list").childNodes[vid_index];
  let img = numb.childNodes[0];
  img.src = "../images/checked.png";
  // console.log(selected.children()[1].textContent)
});
for (
  let index = 1;
  index <= course_content[course_index].links.length;
  index++
) {
  container.childNodes[index].childNodes[1].addEventListener(
    "click",
    function () {
      vid_index = index;
      video.src = course_content[course_index].links[index - 1];
      video_title.textContent =
        course_content[course_index].vid_names[index - 1];
    }
  );
}
